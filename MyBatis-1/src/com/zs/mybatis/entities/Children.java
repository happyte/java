package com.zs.mybatis.entities;

public class Children {
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Children [id=" + id + ", name=" + name + "]";
	}
	
}
